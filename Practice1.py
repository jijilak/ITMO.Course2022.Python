Length1 = ((dis4 ** 2) + (dis1 ** 2)) ** (1 / 2)
"""print(f"L1 = {Length1:.6f}")"""
Length2 = (((dis3 - dis4) ** 2) + (dis2 ** 2)) ** (1 / 2)
