import math

"""Переводим в футы: в 1 ярде 3 фута, в одной миле 5280 футов"""
yard_to_feet = 3
milesPerHour_to_feetPerSecond = 1.466666
"""π = pi_"""
pi_ = 3.1415926535897932384626433832795
"""Введите кратчайшее расстояние между спасателем и кромкой воды, d1 (ярды) => 8
dis1 = 8 * 3"""
dis1 = yard_to_feet * float(input("Введите кратчайшее расстояние между спасателем и кромкой воды, d1 (ярды) => 8: "))
"""Введите кратчайшее расстояние от утопающего до берега, d2 (футы) => 10
dis2 = 10"""
dis2 = float(input("Введите кратчайшее расстояние от утопающего до берега, d2 (футы) => 10: "))
"""Введите боковое смещение между спасателем и утопающим, h (ярды) => 50
dis3 = 50 * 3"""
dis3 = yard_to_feet * float(input("Введите боковое смещение между спасателем и утопающим, h (ярды) => 50: "))
"""Введите скорость движения спасателя по песку, v_sand (мили в час) => 5
v_sand = 5 * 1.466666 #футы в секунду"""
v_sand = milesPerHour_to_feetPerSecond * float(input("Введите скорость движения спасателя по песку,"
                                                     "v_sand (мили в час) => 5: "))
"""Введите коэффициент замедления спасателя при движении в воде, n => 2
coefficient = 2"""
coefficient = float(input("Введите коэффициент замедления спасателя при движении в воде, n => 2: "))
"""Введите направление движения спасателя по песку, θ1_ (градусы) => 39.413
theta1 = 39.413"""
theta1 = float(input("Введите направление движения спасателя по песку, θ1 (градусы) => 39.413: "))


print("Решение: ")

