print(f"Кратчайшее расстояние между спасателем и кромкой воды, d1 (ярды) => {dis1 / yard_to_feet :.0f}")

print(f"Кратчайшее расстояние от утопающего до берега, d2 (футы) => {dis2 :.0f}")

print(f"Боковое смещение между спасателем и утопающим, h (ярды) => {dis3 / yard_to_feet :.0f}")

print(f"Скорость движения спасателя по песку, v_sand (мили в час) => {v_sand / milesPerHour_to_feetPerSecond :.0f}")

print(f"Коэффициент замедления спасателя при движении в воде, n => {coefficient :.0f}")

